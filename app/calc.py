def soma(a, b):
    # return a - b  # ERRO proposital
    return a + b  # Correção

def subtracao(a, b):
    return a - b

def multiplica(a, b):
    return a * b

def divisao(a, b):
    return a / b

def potencia(base, expoente):
    """Retorna base elevado ao expoente."""
    return base ** expoente