def soma(a, b):
    # return a - b  # ERRO proposital
    return a + b  # Correção

def subtracao(a, b):
    return a - b

def multiplica(a, b):
    return a * b

def divisao(a, b):
    return a / b

def potencia(a, b):
    return a ** b

def modulo(a, b):
    return a % b

import math

def raiz_quadrada(a):
    return math.sqrt(a)

